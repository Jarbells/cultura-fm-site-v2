import { defineConfig } from 'vite'
import react from '@vitejs/plugin-react'

// https://vitejs.dev/config/
export default defineConfig({
  plugins: [react()],
  server: {
    port: 5173, // Mantém a porta do site público
    proxy: {
      // Com esta configuração, um pedido a /api/sponsors será corretamente
      // encaminhado para http://localhost:8080/api/sponsors
      '/api': {
        target: 'http://localhost:8080',
        changeOrigin: true
        // A linha 'rewrite' foi removida
      }
    }
  }
})